package examples;

public class B {
	//Now what all wrapper classes we have are,
	public static void main(String[] args) {
		Byte b = 10; //Or another way we can store the value as 'Byte b = new Byte(10);'
		Short s = 20;
		Integer i = 30;
		Long l = 40L;
		Float f = 10.3F;
		Double d = 10.3;
		Character c = 'a';
		Boolean o = true;
		//String is by default a class.
		//Every data-type has a corresponding Wrapper Class.
	}
}
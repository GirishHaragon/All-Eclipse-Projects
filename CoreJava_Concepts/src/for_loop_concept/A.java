package for_loop_concept;

public class A {
	public static void main(String[] args) {
		int x=10;
		for (int i = 0; i <= 3; i++) { //The for loop runs from 0 to 3 times as per given condition.
			System.out.println(i);
			System.out.println("Hello!!");
			System.out.println(x);
		}
		
		for (int i = 0; i < 5; i++) {//this loop runs from 0 to 4 times.
			System.out.println("Hello");
		}
	}
}
